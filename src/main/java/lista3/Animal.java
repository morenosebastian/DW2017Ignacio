package lista3;

public class Animal {
	
	public String nome;
	public double peso;
	public String tipo;

	public Animal(String nome, double peso, String tipo) {
		super();
		this.nome = nome;
		this.peso = peso;
		this.tipo = tipo;
	}

	public Animal(String nome) {
		this.nome = nome;
	}

	public String getNome() {
		return nome;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}

	public double getPeso() {
		return peso;
	}

	public void setPeso(double peso) {
		this.peso = peso;
	}

	public String getTipo() {
		return tipo;
	}

	public void setTipo(String tipo) {
		this.tipo = tipo;
	}
	
}

